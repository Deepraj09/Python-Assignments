def Add(Num1, Num2):
    Ans = 0
    Ans = Num1 + Num2
    return Ans

def Mul(Num1, Num2):
    Ans = 0
    Ans = Num1 * Num2
    return Ans

def Div(Num1, Num2):
    Ans = 0
    Ans = Num1 / Num2
    return Ans

def Sub(Num1, Num2):
    Ans = 0
    Ans = Num1 - Num2
    return Ans

